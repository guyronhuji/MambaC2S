#!/usr/bin/env python3
"""Summarize all experiment results into a comparison table.

Scans ``outputs/`` for completed experiments, reads their metrics, and
produces:
  - ``outputs/summary.csv``     — machine-readable table
  - ``outputs/summary.md``      — markdown table for reports

Usage::

    python scripts/summarize_results.py
    python scripts/summarize_results.py --output-dir outputs/ --out outputs/summary
"""

from __future__ import annotations

import argparse
import json
import logging
import sys
from pathlib import Path

import pandas as pd

sys.path.insert(0, str(Path(__file__).parent.parent))

from src.utils.config import load_config
from src.utils.logging import setup_logging


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Summarize experiment results.")
    p.add_argument("--output-dir", default="outputs/", help="Root outputs directory.")
    p.add_argument("--out", default=None,
                   help="Output path prefix (without extension). "
                        "Defaults to <output-dir>/summary")
    return p.parse_args()


def _load_experiment(exp_dir: Path) -> dict | None:
    """Extract key fields from one experiment directory."""
    config_path = exp_dir / "config_resolved.yaml"
    checkpoint = exp_dir / "best_checkpoint.pt"
    ss_path = exp_dir / "metrics" / "self_supervised.json"
    ds_path = exp_dir / "metrics" / "downstream.json"
    pert_path = exp_dir / "metrics" / "perturbation.json"
    train_summary = exp_dir / "training_summary.json"

    if not (config_path.exists() and checkpoint.exists()):
        return None  # incomplete experiment

    row: dict = {"exp_id": exp_dir.name, "checkpoint": str(checkpoint)}

    try:
        cfg = load_config(config_path)
        row["model_type"] = cfg["model"]["type"]
        row["tokenization"] = cfg["tokenization"]["scheme"]
        row["d_model"] = cfg["model"]["d_model"]
        row["n_layers"] = cfg["model"]["n_layers"]
    except Exception:
        pass

    if train_summary.exists():
        with open(train_summary) as f:
            ts = json.load(f)
        row["best_epoch"] = ts.get("best_epoch")
        row["total_epochs"] = ts.get("total_epochs")
        row["best_val_loss"] = ts.get("best_val_loss")

    if ss_path.exists():
        with open(ss_path) as f:
            ss = json.load(f)
        row["val_loss"] = ss.get("loss")
        row["val_perplexity"] = ss.get("perplexity")

    if ds_path.exists():
        with open(ds_path) as f:
            ds = json.load(f)
        row["ARI"] = ds.get("ARI")
        row["NMI"] = ds.get("NMI")
        row["knn_purity"] = ds.get("knn_purity")

    if pert_path.exists():
        with open(pert_path) as f:
            pert = json.load(f)
        row["pert_mean_cosine_dist"] = pert.get("mean_cosine_distance")

    return row


def main() -> None:
    args = parse_args()
    setup_logging()
    logger = logging.getLogger(__name__)

    output_dir = Path(args.output_dir)
    if not output_dir.exists():
        logger.error("Output directory does not exist: %s", output_dir)
        sys.exit(1)

    rows = []
    for exp_dir in sorted(output_dir.iterdir()):
        if not exp_dir.is_dir():
            continue
        row = _load_experiment(exp_dir)
        if row is not None:
            rows.append(row)

    if not rows:
        logger.warning("No complete experiments found in %s.", output_dir)
        return

    df = pd.DataFrame(rows)
    # Sort by ARI descending (best model first), fallback to val_loss
    sort_col = "ARI" if "ARI" in df.columns else "val_loss"
    ascending = sort_col == "val_loss"
    df = df.sort_values(sort_col, ascending=ascending, na_position="last")

    out_prefix = Path(args.out) if args.out else output_dir / "summary"
    csv_path = out_prefix.with_suffix(".csv")
    md_path = out_prefix.with_suffix(".md")

    df.to_csv(csv_path, index=False)
    logger.info("Summary CSV saved to %s.", csv_path)

    _save_markdown(df, md_path)
    logger.info("Summary Markdown saved to %s.", md_path)

    # Print to console
    print(df.to_string(index=False))


def _save_markdown(df: pd.DataFrame, path: Path) -> None:
    lines = ["# Experiment Summary\n"]
    lines.append(df.to_markdown(index=False, floatfmt=".4f"))
    lines.append("\n\n*Generated by summarize_results.py*\n")
    path.write_text("\n".join(lines))


if __name__ == "__main__":
    main()
